//operadores lógicos
const condicaoA = true; 
const condicaoB = true;

//comparação AND eu uso este simbolo &&
console.log(condicaoA && condicaoB);

//comparação OR eu uso este simbolo ||
console.log(condicaoA || condicaoB);

//se eu quiser negar algo eu uso a exclamação na frente do boleano
const condicao3 = !true;
console.log(condicaoA && condicao3);